double plus(double &x, double &y)
{
    return x + y;
};

double minus(double &x, double &y)
{
    return x - y;
};

double mult(double &x, double &y)
{
    return x * y;
};

double devide(double &x, double &y)
{
    return x / y;
};

double percent(double &x, double &y)
{
    return x / 100 * y;
};
